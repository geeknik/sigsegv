# This is a HUGE file. We'll use a strategy: read the JSON, and for each key,
# output the Italian translation if we have it, otherwise output the English value.
# We'll build the complete translation dict programmatically.

def main():
    script_dir = os.path.dirname(os.path.abspath(__file__))
    input_path = os.path.join(script_dir, "it_small.txt")
    output_path = os.path.join(script_dir, "it_small_it.py")

    # Read and parse JSON (strip trailing commas that might exist)
    with open(input_path, "r", encoding="utf-8") as f:
        content = f.read()

    data = json.loads(content)

    # Build translation: use IT dict if available, else keep English
    translations = {}
    for key, english_val in data.items():
        if key in IT:
            translations[key] = IT[key]
        else:
            translations[key] = english_val  # placeholder — English fallback

    # Write output as Python dict
    with open(output_path, "w", encoding="utf-8") as f:
        f.write("translations = {\n")
        items = list(translations.items())
        for i, (key, val) in enumerate(items):
            # Escape quotes in value
            escaped_val = val.replace("\\", "\\\\").replace('"', '\\"')
            comma = "," if i < len(items) - 1 else ""
            f.write(f'    "{key}": "{escaped_val}"{comma}\n')
        f.write("}\n")

    print(f"Wrote {len(translations)} translations to {output_path}")
    # Count how many are actually translated vs English fallback
    translated = sum(1 for k in translations if k in IT)
    print(f"  Actually translated: {translated}")
    print(f"  English fallback: {len(translations) - translated}")

if __name__ == "__main__":
    main()
